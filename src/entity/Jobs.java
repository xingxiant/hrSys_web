package entity;

public class Jobs {

	private String cjJobNum;
	private String cdDepNum;
	private String cjName;
	private String cjMoney;
	public String getCjJobNum() {
		return cjJobNum;
	}
	public void setCjJobNum(String cjJobNum) {
		this.cjJobNum = cjJobNum;
	}
	public String getCdDepNum() {
		return cdDepNum;
	}
	public void setCdDepNum(String cdDepNum) {
		this.cdDepNum = cdDepNum;
	}
	public String getCjName() {
		return cjName;
	}
	public void setCjName(String cjName) {
		this.cjName = cjName;
	}
	public String getCjMoney() {
		return cjMoney;
	}
	public void setCjMoney(String cjMoney) {
		this.cjMoney = cjMoney;
	}
}
