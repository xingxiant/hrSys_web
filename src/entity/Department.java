package entity;

public class Department {

	private String cdDepNum;
	private String cdName;
	private String cdPNum;
	private String cdBoss;
	private String cdTel;
	public String getCdDepNum() {
		return cdDepNum;
	}
	public void setCdDepNum(String cdDepNum) {
		this.cdDepNum = cdDepNum;
	}
	public String getCdName() {
		return cdName;
	}
	public void setCdName(String cdName) {
		this.cdName = cdName;
	}
	public String getCdPNum() {
		return cdPNum;
	}
	public void setCdPNum(String cdPNum) {
		this.cdPNum = cdPNum;
	}
	public String getCdBoss() {
		return cdBoss;
	}
	public void setCdBoss(String cdBoss) {
		this.cdBoss = cdBoss;
	}
	public String getCdTel() {
		return cdTel;
	}
	public void setCdTel(String cdTel) {
		this.cdTel = cdTel;
	}
	
}
