package entity;

public class Education {

	private String seNum;
	private String seDegree;
	private String seMajor;
	private String seSchool;
	private int seGraTime;
	private String seLanLevel;
	public String getSeNum() {
		return seNum;
	}
	public void setSeNum(String seNum) {
		this.seNum = seNum;
	}
	public String getSeDegree() {
		return seDegree;
	}
	public void setSeDegree(String seDegree) {
		this.seDegree = seDegree;
	}
	public String getSeMajor() {
		return seMajor;
	}
	public void setSeMajor(String seMajor) {
		this.seMajor = seMajor;
	}
	public String getSeSchool() {
		return seSchool;
	}
	public void setSeSchool(String seSchool) {
		this.seSchool = seSchool;
	}
	public int getSeGraTime() {
		return seGraTime;
	}
	public void setSeGraTime(int seGraTime) {
		this.seGraTime = seGraTime;
	}
	public String getSeLanLevel() {
		return seLanLevel;
	}
	public void setSeLanLevel(String seLanLevel) {
		this.seLanLevel = seLanLevel;
	}
	
}
